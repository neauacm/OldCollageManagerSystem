package xin.miku.hat.sessionbean;

public class Sessionbean {
	private String sessionname;
	
	private String sessionbeginmtime;
	
	private String sessionendtime;
	
	private String sessiontime;
	
	private String sessionid; 
	
	private String sessionclass;
	
	public String getSessionname() {
		return sessionname;
	}

	public void setSessionname(String sessionname) {
		this.sessionname = sessionname;
	}

	public String getSessionbeginmtime() {
		return sessionbeginmtime;
	}

	public void setSessionbeginmtime(String sessionbeginmtime) {
		this.sessionbeginmtime = sessionbeginmtime;
	}

	public String getSessionendtime() {
		return sessionendtime;
	}

	public void setSessionendtime(String sessionendtime) {
		this.sessionendtime = sessionendtime;
	}

	public String getSessiontime() {
		return sessiontime;
	}

	public void setSessiontime(String sessiontime) {
		this.sessiontime = sessiontime;
	}

	public String getSessionid() {
		return sessionid;
	}

	public void setSessionid(String sessionid) {
		this.sessionid = sessionid;
	}

	public String getSessionclass() {
		return sessionclass;
	}

	public void setSessionclass(String sessionclass) {
		this.sessionclass = sessionclass;
	}

}
